package DTO;

public class VaiTro {
    private String maVT;
    private String tenVT;

    public VaiTro() {

    }
    public VaiTro(String maVT, String tenVT) {
        this.maVT = maVT;
        this.tenVT = tenVT;
    }

    public String getMaVT() {
        return maVT;
    }

    public String getTenVT() {
        return tenVT;
    }
}

